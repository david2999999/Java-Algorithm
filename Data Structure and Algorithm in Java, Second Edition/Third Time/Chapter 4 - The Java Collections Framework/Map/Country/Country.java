class Country {
    private String name;
    private String language;
    private int area;
    private int population;
    private double avAge;

    Country(String name, String lang, int area, int pop, double avAge) {
        this.name = name;
        this.language = lang;
        this.area = area;
        this.population = pop;
        this.avAge = avAge;
    }
}